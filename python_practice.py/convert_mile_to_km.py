#Receive miles and convert to kilometers
#Ask the user to input miles and assign it to the miles variable
miles = input("enter number of miles: ")
#Kilometers = miles * 1.60934
#convert miles to kilometers and save in a variable called kilometers
#convert string to integer
kilometers = int(miles) * 1.60934
#enter miles 5
#output 5 mile = 8.0467 kilometers
#Print results using format
print("{} miles = {} kilometers".format(miles, kilometers))